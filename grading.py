#4 different variables
kg_value_1 = 2.4
kg_value_2 = 9.1
kg_value_3 = 4.7
kg_value_4 = 1.0

#conversion factor pounds(lb) = kilograms(kg) * 2.20462
pounds_1 = kg_value_1 * 2.20462
pounds_2 = kg_value_2 * 2.20462
pounds_3 = kg_value_3 * 2.20462
pounds_4 = kg_value_4 * 2.20462

#print statments for conversions
print(f"{kg_value_1} kilograms is equal to {pounds_1:.2f} pounds.")
print(f"{kg_value_2} kilograms is equal to {pounds_2:.2f} pounds.")
print(f"{kg_value_3} kilograms is equal to {pounds_3:.2f} pounds.")
print(f"{kg_value_4} kilograms is equal to {pounds_4:.2f} pounds.")